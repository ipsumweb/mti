          <p>Proceed with WR as you would with any of the previous materials. We place an additional layer of resin on top of the existing laminate, double-checking there are no air bubbles by rolling out our resin. The process is quite repetitive. The final layer in this section is the CSM and more resin. You've finished the structural section of your FRP! It is important to note that the strength, which may require multiple applications of CSM/WR depending on the final design requirements, is fully achieved in this layer.</p>

<p>Please remember that in this example, there are 3 sections with not many fiber layers that comprise the end fiberglass product. The actual number and type of layers of fibers in each section will vary depending on the design requirements of your vessel.</p>
